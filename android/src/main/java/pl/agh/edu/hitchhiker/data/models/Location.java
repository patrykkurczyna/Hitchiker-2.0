package pl.agh.edu.hitchhiker.data.models;

public class Location {
    private String href;

    public Location() {
    }

    public Location(String href) {
        this.href = href;
    }

    public String getHref() {
        return href;
    }

    public void setHref(String href) {
        this.href = href;
    }
}
